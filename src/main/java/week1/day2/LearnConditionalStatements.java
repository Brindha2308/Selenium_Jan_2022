package week1.day2;

public class LearnConditionalStatements {
public static void main(String[] args) {
	// Conditional Statement -> make a decision in their application
	int age = 8;
	if (age >= 18) { //condition -> true output
		System.out.println("The candidate is eligible for voting");
	} else {	//condition -> false output
		System.out.println("The candidate is not eligible for voting");
	}
	
	
	System.out.println("This is out of the condition");
	
}
}
